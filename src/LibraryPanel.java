    private void jbViewBooksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbViewBooksActionPerformed
        panel_view.setVisible(true);
        int index = 0;
       
        DefaultTableModel model1 = (DefaultTableModel)table_displayAllBooks.getModel();
        
        model1.setRowCount(0);
      
        while (BookAction.book.size() > index) {
           model1.addRow(new Object[]{BookAction.book.get(index).getBookId(),
               BookAction.book.get(index).getBookName(),BookAction.book.get(index).getAuthor(),
               BookAction.book.get(index).getPublisher(),BookAction.book.get(index).getYearOfPublication()}); 
           index++;
        }
       
    }//GEN-LAST:event_jbViewBooksActionPerformed

