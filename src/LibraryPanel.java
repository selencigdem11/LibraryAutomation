    private void addToUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToUserActionPerformed
        String id = searchedId.getText();
        Books book = bookAction.searchBook(Integer.parseInt(id));
        User.getMyBooks().add(book);
        message1.setText("Your book is added!");
        
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        Date currentDate = new Date();
        Calendar gettingDate;
        
        message3.setText("Current date: " + sdf.format(currentDate));
        
        Calendar c = Calendar.getInstance();
        c.setTime(currentDate);
        
        c.add(Calendar.DATE, 15);
        
        Date returningDate = c.getTime();
        
        message4.setText("Returning date: " + sdf.format(returningDate));
        
        
        
       
        
        
    }//GEN-LAST:event_addToUserActionPerformed

